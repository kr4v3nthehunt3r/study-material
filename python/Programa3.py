# 1. Pede ao usuário as duas notas e as converte para número decimal (float)
nota1 = float(input("Digite a primeira nota: "))
nota2 = float(input("Digite a segunda nota: "))

# 2. Calcula a média
media = (nota1 + nota2) / 2

# 3. Mostra o resultado formatado
print(f"Média: {media}")

